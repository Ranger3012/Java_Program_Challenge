class Enhanced_Loop{
    public static void main(String[] args) {
        int prime[] = {2, 3, 5, 7, 11};
        for (int i : prime){
            System.out.println(i);
        }

        String lang[] = { "C", "JAVA", "PYTHON"};
        for(String sample : lang){
            System.out.println(sample);
        }
    }
}